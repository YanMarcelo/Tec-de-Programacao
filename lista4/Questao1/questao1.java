package Questao1;

//Classe: É um tipo de estrutura que representa “objetos” com suas características e comportamentos, conhecidos como atributos e métodos. Ela é o início do código que vai servir para criar um objeto.

//Objeto: O objeto é um conjunto de dados que estão ligados com a classe,ele é capaz de executar métodos de acordo com a classe, e esse métodos podem modificar os dados que lhes foram atribuídos.

//Método: É uma sequência de instruções que podem ser executadas quando solicitado, eles podem ser usados para realizar ações específicas e retornar valores ou nada.

//Atributo: É responsável por armazenar dados dentro da classe e cada objeto pode possuir diferentes tipos de atributos. Geralmente estão relacionados às características dos objetos.

//Encapsulamento: O encapsulamento é responsável por proteger os dados de determinadas classes, assim controlando o acesso aos dados. Definindo atributos ou métodos como privados, públicos e protegidos. 

//Visibilidade de Atributos e Métodos: Se refere a capacidade de outras classes ou objetos acessarem elementos. E eles se dividem em:
//Public: Pode ser acessado a qualquer momento, por qualquer classe ou objeto.
//Private: Só pode ser acessado dentro da mesma classe.
//Protected: Só pode ser acessado dentro do mesmo pacote.
//Default: Quando uma classe ou objeto não possui uma visibilidade definida, é considerada default, seguindo assim um princípio parecido com protected.

//Herança: A herança permite que classes chamadas “filhas” possam herdar atributos e métodos de classes “pai”.

//Polimorfismo: Quando um objeto da classe filha é tratado como um objeto da classe pai, e classe filha pode ser tratada como parte da classe pai.

//Classes abstratas: É uma classe que pode servir como base para outras classes que pode ou não conter métodos abstratos, ela é útil quando outras classes possuem atributos ou métodos em comum.

//Construtores: O construtor serve para inicializar um objeto assim que ele é criado, geralmente  possui o mesmo nome da classe e não retorna nada. Quando se passa atributo dentro do construtor, ao criar o objeto, obrigatoriamente se deve colocar os mesmos atributos.

//Get e Set: São utilizados para acessar ou modificar dados quando necessário, geralmente são chamados após o encapsulamento de determinado objeto. O get retorna o atributo de algum objeto, já o Set modifica o valor do atributo quando necessário. 

//Sobrecarga de métodos: É utilizada para definir métodos com o mesmo nome mas parâmetros de tipos diferentes, permitindo que haja a mesma funcionalidade de métodos mas com tipos diferentes.
//
//Sobrescrita de métodos: É utilizada quando uma classe filha reescreve os métodos recebidos de uma classe pai, para se adequar aos seus objetos.

//Palavras reservadas: São palavras que possuem funcionalidades específicas e que não podem ser usadas como variáveis, métodos ou classes. Exemplos: 
//Super: É usada para acessar membros da classe pai a partir de uma classe filha.
//This: É usado para se referir ao objeto atual da classe em que o código está sendo executado.
//Final: É usado para declarar uma constante ou indicar que uma classe, método ou variável não pode ser alterado ou sobrescrito. 

//Relação de objetos: “ter”, “usar” e “ser”: 
//Ter (Associação): Ocorre quando uma classe possui uma referência para outra classe como atributo. 
//Usar (Dependência): Ocorre quando uma classe usa outra para realizar uma tarefa, mas não possui uma referência para essa classe como atributo.
//Ser(Herança): Ocorre quando uma classe filha herda atributos e métodos de uma classe pai.  
